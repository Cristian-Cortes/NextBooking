/*====================Leer Mas 1======================================================*/
let leer = 0;
let button = document.querySelector('#btn-read').addEventListener('click', function(){
	if (!leer) {
		document.getElementById('ReadMore').style.display = 'inline';
		document.getElementById('btn-read').innerHTML = 'Leer Menos';
		leer = 1;
	}else{
		document.getElementById('ReadMore').style.display = 'none';
		document.getElementById('btn-read').innerHTML = 'Leer Mas';
		leer = 0;
	}
})
